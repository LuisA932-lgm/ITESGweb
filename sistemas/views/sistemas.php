                                                <h3>OBJETIVO</h3>
                                                <p>Formar profesionistas líderes con visión estratégica y amplio sentido ético; capaz de diseñar, desarrollar, implementar y administrar tecnología computacional para aportar soluciones innovadoras en beneficio de la sociedad; en un contexto global, multidisciplinario y sostenible.
                                                </p>
                                                <h3>PERFIL DE INGRESO </h3>
                                                <p>El aspirante debe reunir los conocimientos, habilidades y actitudes necesarias para cursar una ingeniería en el nivel superior, haber concluido los estudios de bachillerato en el nivel medio superior para responder adecuadamente a las exigencias de los planes de estudio que ofrece el ITESG.</p>
                                                <p>Conocimientos básicos:</p>                                                
                                                    <li>Ciencias básicas: Matemáticas, Física y Química</li>
                                                    <li>Computación básica</li>
                                                <p></p>
                                                <p>Habilidades: </p>                                                
                                                    <li>Capacidad para razonar matemáticamente.</li>
                                                    <li>Creatividad y capacidad de emprender.</li>
                                                    <li>Aplicar el ingenio en la resolución de problemas.</li>
                                                    <li>Capacidad de análisis y síntesis</li>
                                                    <li>Tecnológicas básicas</li>      
                                                <p></p>                                          
                                                <h3>PERFIL DE EGRESO</h3>                                               
                                                <ol>
                                                    <li>Implementa aplicaciones computacionales para solucionar problemas de diversos contextos, integrando diferentes tecnologías, plataformas o dispositivos.</li>
                                                    <p></p>
                                                    <li>Diseña, desarrolla y aplica modelos computacionales para solucionar problemas, mediante la selección y uso de herramientas matemáticas.</li>
                                                    <p></p>
                                                    <li>Diseña e implementa interfaces para la automatización de sistemas de hardware y desarrollo del software asociado.</li>
                                                    <p></p>
                                                    <li>Coordina y participa en equipos multidisciplinarios para la aplicación de soluciones innovadoras en diferentes contextos.</li>
                                                    <p></p>
                                                    <li>Diseña, implementa y administra bases de datos optimizando los recursos disponibles, conforme a las normas vigentes de manejo y seguridad de la información.</li>
                                                    <p></p>
                                                    <li>Desarrolla y administra software para apoyar la productividad y competitividad de las organizaciones cumpliendo con estándares de calidad.</li>
                                                    <p></p>
                                                    <li>Evalúa tecnologías de hardware para soportar aplicaciones de manera efectiva.</li>
                                                    <p></p>
                                                    <li>Detecta áreas de oportunidad empleando una visión empresarial para crear proyectos aplicando las Tecnologías de la Información y Comunicación.</li>
                                                    <p></p>
                                                    <li>Diseña, configura y administra redes de computadoras para crear soluciones de conectividad en la organización, aplicando las normas y estándares vigentes.</li>
                                                </ol>
                                                <h3>CAMPO LABORAL</h3>
                                                <p><li>Industria 4.0 instalada en la región.</li>
                                                    <li>Sectores especializados en tecnologías de Información y Telecomunicaciones</li>
                                                    <li>En cualquier sector y sea industrial, gubernamental o de servicios</li>
                                                <p></p>  
                                                <h3>MISIÓN</h3>
                                                <p>La carrera de Ingeniería en Sistemas Computacionales del ITESG forma ingenieros con un enfoque integral mediante un modelo basado en competencias fomentando el desarrollo tecnológico, investigación y proyectos multidisciplinarios en el área de Tecnologías de Información y comunicación, que contribuyen al impulso social, económico y sustentable del entorno.</p>
                                                <h3>VISIÓN</h3>
                                                <p>La carrera de Ingeniería en Sistemas Computacionales es un programa académico reconocido por su calidad en la formación de profesionales que aplican herramientas y técnicas esenciales en el área de Tecnologías de Información y Comunicaciones en su ámbito laboral, atendiendo la demanda del sector productivo y social a nivel regional, nacional e internacional.</p>
                                                <h3>VALORES</h3>
                                                <p></p>
                                                <p>La comunidad tecnológica de la Ingeniería en Sistemas Computacionales, nos comprometemos a cumplir con los siguientes valores, que deben regir siempre nuestra conducta:</p>
                                                <li>
                                                    <b>Responsabilidad: </b>Cumplir con las funciones y tareas, individual y grupalmente, con un sentido de compromiso y un enfoque centrado en la calidad.
                                                </li>
                                                <p></p>
                                                <li><b>Veracidad: </b>Actuar de conformidad con la verdad, con honestidad y transparencia en la ejecución de nuestros trabajos.
                                                </li>
                                                <p></p>
                                                <li><b>Profesionalismo: </b>Ser ético, respetuoso, competente y mantenerse actualizado constantemente. </li>
                                                <p></p>
                                                <h3>PLAN DE ESTUDIOS</h3>
                                                <p>El Plan de Estudios vigente en la carrera de Ingeniería en Sistemas Computacionales tiene la clave ISIC-2010-224, ha estado vigente desde agosto de 2010, acorde al oficio No. 513.1/1637/10, emitido por la dirección general de educación tecnológica, aplica para todos los estudiantes inscritos corresponde a 260 créditos que se componen de: 210 (estructura genérica 49 materias), 25 (módulo de especialidad 5 materias), 10 (programa de residencias), 10 (servicio social), 5 (actividades complementarias).</p>
