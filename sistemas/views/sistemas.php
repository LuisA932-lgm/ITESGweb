                                                <p><li>Industria 4.0 instalada en la región.</li>
                                                    <li>Sectores especializados en tecnologías de Información y Telecomunicaciones</li>
                                                    <li>En cualquier sector y sea industrial, gubernamental o de servicios</li>
                                                <p></p>  
                                                <h3>MISIÓN</h3>
                                                <p>La carrera de Ingeniería en Sistemas Computacionales del ITESG forma ingenieros con un enfoque integral mediante un modelo basado en competencias fomentando el desarrollo tecnológico, investigación y proyectos multidisciplinarios en el área de Tecnologías de Información y comunicación, que contribuyen al impulso social, económico y sustentable del entorno.</p>
                                                <h3>VISIÓN</h3>
                                                <p>La carrera de Ingeniería en Sistemas Computacionales es un programa académico reconocido por su calidad en la formación de profesionales que aplican herramientas y técnicas esenciales en el área de Tecnologías de Información y Comunicaciones en su ámbito laboral, atendiendo la demanda del sector productivo y social a nivel regional, nacional e internacional.</p>
                                                <h3>VALORES</h3>
                                                <p></p>
                                                <p>La comunidad tecnológica de la Ingeniería en Sistemas Computacionales, nos comprometemos a cumplir con los siguientes valores, que deben regir siempre nuestra conducta:</p>
                                                <li>
                                                    <b>Responsabilidad: </b>Cumplir con las funciones y tareas, individual y grupalmente, con un sentido de compromiso y un enfoque centrado en la calidad.
                                                </li>
                                                <li><b>Veracidad: </b>Actuar de conformidad con la verdad, con honestidad y transparencia en la ejecución de nuestros trabajos.
                                                </li>
                                                <li><b>Profesionalismo: </b>Ser ético, respetuoso, competente y mantenerse actualizado constantemente. </li>
