                                                <h3>OBJETIVO</h3>
                                                <p>Formar profesionistas líderes con visión estratégica y amplio sentido ético; capaz de diseñar, desarrollar, implementar y administrar tecnología computacional para aportar soluciones innovadoras en beneficio de la sociedad; en un contexto global, multidisciplinario y sostenible.
                                                </p>
                                                <h3>PERFIL DE INGRESO </h3>
                                                <p>El aspirante debe reunir los conocimientos, habilidades y actitudes necesarias para cursar una ingeniería en el nivel superior, haber concluido los estudios de bachillerato en el nivel medio superior para responder adecuadamente a las exigencias de los planes de estudio que ofrece el ITESG.</p>
