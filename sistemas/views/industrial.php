                                                <h3>OBJETIVO</h3>
                                                <p>Formar profesionales que contribuyan al desarrollo sustentable, con una visión sistémica, que corresponda a los retos que presentan constantes cambios en los sistemas de producción de bienes y servicios de manera ética y comprometidos con la sociedad.
                                                Esta carrera surge como una opción para aumentar la competitividad de nuestro país, aprovechando de la mejor manera posible los recursos naturales, materiales y humanos, lo que contribuye a elevar el desarrollo organizacional y sus niveles de calidad.
                                                </p>
                                                <h3>PERFIL DE INGRESO </h3>
                                                <p>Los futuros estudiantes de ingeniería industrial del ITESG son hombres y mujeres egresados del nivel medio superior que buscan integrarse a una institución formadora de ingenieros, que les permita aplicar sus conocimientos previos ayudando al desarrollo de la industria y la sociedad de manera integral y con espíritu de servicio.</p>
                                                <h3>PERFIL DE EGRESO</h3>
                                                <p>El egresado de este programa educativo es capaz de:</p>
                                                    <li>Diseñar, implementar y mejorar sistemas integrados de abastecimiento, producción y distribución de organizaciones productoras de bienes y servicios, de forma sustentable considerando requisitos normativos y estándares nacionales e internacionales.</li>
                                                    <p></p>
                                                    <li>Aplicar las habilidades adquiridas para utilizar de manera óptima la infraestructura disponible en las organizaciones.</li>
                                                    <p></p>
                                                    <li>Participar, promover y/o coordinar proyectos de transferencia, asimilación, desarrollo y adaptación a las nuevas tecnologías.</li>
                                                    <p></p>
                                                    <li>Integrar, dirigir y mantener equipos inter y multidisciplinarios en ambientes cambiantes y multiculturales.</li>
                                                    <p></p>
                                                    <li>Planear y diseñar la localización y distribución de instalaciones, así como su mantenimiento para la producción de bienes y servicios.</li>
                                                    <p></p>
                                                    <li>Seleccionar, instalar y poner en marcha equipos y maquinaria de producción.</li>
                                                    <p></p>
                                                    <li>Integrar, implementar y administrar sistemas de higiene y seguridad, gestión de riesgos, generando un ambiente ergonómico y saludable.</li>
                                                    <p></p>
                                                    <li>Gestionar proyectos de inversión innovadores, creativos y útiles que sean pertinentes al entorno en el que se desenvuelven con alto sentido de ética.</li>
                                                    <p></p>
                                                    <li>Diseñar, administrar, implementar y liderar sistemas de gestión de calidad, seguridad y medio ambiente.</li>
                                                    <p></p>
                                                    <li>Aplicar de forma adecuada las herramientas de redacción, lectura, y ortografía.</li>
                                                    <p></p>
                                                    <li>Se comunica de forma asertiva lo cual le permite interactuar de forma positiva generando una buena presentación personal y profesional, así como la capacidad de liderazgo y trabajo en equipo.</li>
                                                    <p></p>
                                                    <li>Se adapta a las circunstancias del entorno demostrando el uso adecuado del tiempo, trabajo bajo presión y tolerancia a la frustración.</li>
                                                    <p></p>
                                                    <h3>CAMPO LABORAL</h3>
                                                <p>La carrera de Ingeniería Industrial ofrece un amplio campo de trabajo que abarca tanto el sector productivo como el de servicios, en empresas de diferentes tamaños. Con una visión sistémica y habilidades multidisciplinares, los ingenieros industriales son capaces de operar y optimizar equipos, materiales, recursos humanos y sistemas de información. 
                                                Esto les permite desempeñarse en industrias como la automotriz, aeronáutica, plástico, metal-mecánica, construcción, textil, calzado, alimenticia, química, farmacéutica y minera, así como en logística, transporte, servicios de capacitación y asesoría, comerciales, organismos gubernamentales y el sector educativo.
                                                </p>
                                                <h3>MISIÓN</h3>
                                                <p>Formar ingenieros con un enfoque integral mediante un modelo basado en competencias, fomentando el desarrollo tecnológico, la investigación y proyectos multidisciplinarios que contribuyan al desarrollo social, económico y sustentable del entorno.</p>
                                                <h3>VISIÓN</h3>
                                                <p>Ser una unidad académica reconocida como líder en la formación de profesionales, aplicando estándares de calidad en todos sus procesos y respondiendo a la demanda del sector productivo a nivel regional y nacional.</p>
                                                <h3>VALORES</h3>
                                                <p>Interacción personal sana en la institución al asumir las consecuencias de las acciones propias, al:</p>
