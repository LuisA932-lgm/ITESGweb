                <h4>Introducción.</h4>
            </left><br>
            <div align="justify">
                <font face="Arial,Verdana">
                    <p>En <strong>México</strong>, todas las personas son iguales y gozan de los derechos y libertades
                        establecidos en la Constitución; además, los tratados internacionales en la materia les
                        garantizan, sin distinción alguna, el goce de sus derechos civiles, políticos, económicos,
                        sociales, culturales y ambientales. En este sentido, eliminar la discriminación es una condición
                        indispensable para hacer de México una sociedad de derechos.<br><br>

                        En congruencia con lo anterior, en abril del año 2018, el <strong>Instituto Tecnológico Superior
                            de Guanajuato</strong> sometió las condiciones de trabajo que en él prevalecen, a una
                        evaluación bajo los parámetros establecidos en la norma <strong>NMX-R-025-SCFI-2015</strong> en
                        Igualdad Laboral y No Discriminación. Lo anterior, dio como resultado la <strong>obtención de la
                            certificación</strong> en dicha norma.
                    </p>
                </font>
            </div>
            <font face="Arial,Verdana"><br>

